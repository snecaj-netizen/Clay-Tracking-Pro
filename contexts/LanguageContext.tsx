interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language, manual?: boolean) => void;
  t: (key: string) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<Language>(() => {
    try {
      // 1. Priority: check if user has a saved language preference in their profile
      const userStr = localStorage.getItem('auth_user');
      if (userStr) {
        const user = JSON.parse(userStr);
        if (user.language === 'en' || user.language === 'it') {
          return user.language;
        }

        // 2. Secondary Priority: check if user is international
        const isInternational = user.is_international === true || 
                               user.is_international === 'true' || 
                               user.is_international === 1 || 
                               user.is_international === '1';
        
        if (isInternational) {
           const manualChoice = localStorage.getItem('manual_lang_selection');
           const saved = localStorage.getItem('app_language');
           if (manualChoice === 'true' && saved === 'it') return 'it';
           return 'en';
        }
      }

      // 2. Standard saved preference
      const saved = localStorage.getItem('app_language');
      if (saved === 'en' || saved === 'it') return saved as Language;
      
      return 'it';
    } catch (e) {
      console.warn('LocalStorage not available for language preference:', e);
      return 'it';
    }
  });

  useEffect(() => {
    console.log(`Setting language to: ${language}`);
    try {
      localStorage.setItem('app_language', language);
    } catch (e) {
      console.warn('LocalStorage error:', e);
    }
    document.documentElement.lang = language;
  }, [language]);

  const handleSetLanguage = (lang: Language, manual: boolean = true) => {
    if (manual) {
      console.log(`Manual language switch triggered: ${lang}`);
      try {
        localStorage.setItem('manual_lang_selection', 'true');
      } catch (e) {}
    } else {
      console.log(`Automatic language switch triggered: ${lang}`);
    }
    setLanguage(lang);
  };

  const t = (key: string): string => {
    if (!translations[key]) {
      // console.warn(`Translation key missing: ${key}`);
      return key;
    }
    return translations[key][language] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage: handleSetLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};
